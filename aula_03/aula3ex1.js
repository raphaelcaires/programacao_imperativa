function juntarNomes(texto1, texto2) {
    //corpo da função
    return (texto1 + texto2).toUpperCase();

}

//invocando a função
//execução se dá de dentro para fora
console.log(juntarNomes('Raphael', 'Caires'));
console.log(juntarNomes('Fernando', 'Carvalho'));
console.log(juntarNomes('Silvia', 'Fiacador'));

